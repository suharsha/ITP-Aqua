    private void fillSubCatNames() {
        ActivityPlanDAO dao = new ActivityPlanDAO();
        //calling method that is in inside dao
        ArrayList eventList = dao.getEventsWithNoAP();
        Iterator i = eventList.iterator();

        APEventsList.removeAllItems();

        while (i.hasNext()) {
            APEventsList.addItem(i.next());
        }

        //if sub cat all values not selected the first value should load using following code

        APEventsList.setSelectedIndex(0);
    }

